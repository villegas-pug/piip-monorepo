PROMPT [012-reconciliation] Tablas esperadas...
DECLARE
    v_ok PLS_INTEGER := 0;
BEGIN
    FOR r IN (
        WITH esperadas (TABLE_NAME) AS (
            SELECT 'PARTICIPANTE_PERSONA' FROM DUAL UNION ALL
            SELECT 'PROYECTO_PARTICIPANTE_PERSONA' FROM DUAL UNION ALL
            SELECT 'PROYECTO_PARTICIPANTE_UNIDAD' FROM DUAL
        )
        SELECT e.TABLE_NAME, t.TABLE_NAME AS REAL_TABLE_NAME
          FROM esperadas e LEFT JOIN USER_TABLES t ON t.TABLE_NAME = e.TABLE_NAME
         ORDER BY e.TABLE_NAME
    ) LOOP
        IF r.REAL_TABLE_NAME IS NOT NULL THEN v_ok := v_ok + 1; END IF;
        DBMS_OUTPUT.PUT_LINE('TIPO=TABLA;ESPERADA=' || r.TABLE_NAME ||
            ';ENCONTRADA=' || CASE WHEN r.REAL_TABLE_NAME IS NULL THEN 'N' ELSE 'S' END ||
            ';RESULTADO=' || CASE WHEN r.REAL_TABLE_NAME IS NULL THEN 'FALTA' ELSE 'OK' END);
    END LOOP;
    DBMS_OUTPUT.PUT_LINE('RESUMEN=TABLAS;OK=' || v_ok || '/3');
END;
