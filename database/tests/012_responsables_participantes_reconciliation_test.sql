PROMPT [012-reconciliation] PK, UK, FK y CHECK esperados, incluidos estado y referencias...
DECLARE
    v_ok PLS_INTEGER := 0; v_total PLS_INTEGER := 0;
BEGIN
    FOR r IN (
        WITH esperadas (CONSTRAINT_NAME,TABLE_NAME,CONSTRAINT_TYPE,TABLA_REFERIDA) AS (
            SELECT 'PK_PARTICIPANTE_PERSONA','PARTICIPANTE_PERSONA','P',NULL FROM DUAL UNION ALL SELECT 'FK_PP_USUARIO','PARTICIPANTE_PERSONA','R','USUARIO' FROM DUAL UNION ALL SELECT 'CK_PP_CLASIFICACION','PARTICIPANTE_PERSONA','C',NULL FROM DUAL UNION ALL SELECT 'CK_PP_DATOS_MINIMOS','PARTICIPANTE_PERSONA','C',NULL FROM DUAL UNION ALL
            SELECT 'PK_PROYECTO_PARTICIPANTE_PERSONA','PROYECTO_PARTICIPANTE_PERSONA','P',NULL FROM DUAL UNION ALL SELECT 'UK_PPP_PROY_PART','PROYECTO_PARTICIPANTE_PERSONA','U',NULL FROM DUAL UNION ALL SELECT 'FK_PPP_PROYECTO','PROYECTO_PARTICIPANTE_PERSONA','R','PROYECTO' FROM DUAL UNION ALL SELECT 'FK_PPP_PARTICIPANTE','PROYECTO_PARTICIPANTE_PERSONA','R','PARTICIPANTE_PERSONA' FROM DUAL UNION ALL SELECT 'FK_PPP_ACTOR','PROYECTO_PARTICIPANTE_PERSONA','R','USUARIO' FROM DUAL UNION ALL SELECT 'CK_PPP_VIGENCIA','PROYECTO_PARTICIPANTE_PERSONA','C',NULL FROM DUAL UNION ALL
            SELECT 'PK_PROYECTO_PARTICIPANTE_UNIDAD','PROYECTO_PARTICIPANTE_UNIDAD','P',NULL FROM DUAL UNION ALL SELECT 'UK_PPU_PROY_UNI','PROYECTO_PARTICIPANTE_UNIDAD','U',NULL FROM DUAL UNION ALL SELECT 'FK_PPU_PROYECTO','PROYECTO_PARTICIPANTE_UNIDAD','R','PROYECTO' FROM DUAL UNION ALL SELECT 'FK_PPU_UNIDAD','PROYECTO_PARTICIPANTE_UNIDAD','R','UNIDAD_EJECUTORA' FROM DUAL UNION ALL SELECT 'FK_PPU_ACTOR','PROYECTO_PARTICIPANTE_UNIDAD','R','USUARIO' FROM DUAL UNION ALL SELECT 'CK_PPU_VIGENCIA','PROYECTO_PARTICIPANTE_UNIDAD','C',NULL FROM DUAL
        )
        SELECT e.*, c.CONSTRAINT_TYPE REAL_TYPE, c.STATUS, c.VALIDATED, c.SEARCH_CONDITION_VC, rc.TABLE_NAME REAL_TABLA_REFERIDA
          FROM esperadas e LEFT JOIN USER_CONSTRAINTS c ON c.CONSTRAINT_NAME=e.CONSTRAINT_NAME AND c.TABLE_NAME=e.TABLE_NAME LEFT JOIN USER_CONSTRAINTS rc ON rc.CONSTRAINT_NAME=c.R_CONSTRAINT_NAME ORDER BY e.TABLE_NAME,e.CONSTRAINT_NAME
    ) LOOP
        v_total:=v_total+1;
        IF r.REAL_TYPE=r.CONSTRAINT_TYPE AND r.STATUS='ENABLED' AND NVL(r.REAL_TABLA_REFERIDA,'<NULL>')=NVL(r.TABLA_REFERIDA,'<NULL>') THEN v_ok:=v_ok+1; END IF;
        DBMS_OUTPUT.PUT_LINE('TIPO=CONSTRAINT;NOMBRE=' || r.CONSTRAINT_NAME || ';TABLA=' || r.TABLE_NAME || ';TIPO_ESPERADO=' || r.CONSTRAINT_TYPE || ';TIPO_REAL=' || NVL(r.REAL_TYPE,'<NULL>') || ';ESTADO=' || NVL(r.STATUS,'<NULL>') || ';VALIDADO=' || NVL(r.VALIDATED,'<NULL>') || ';REFERENCIA_ESPERADA=' || NVL(r.TABLA_REFERIDA,'<NULL>') || ';REFERENCIA_REAL=' || NVL(r.REAL_TABLA_REFERIDA,'<NULL>') || ';CHECK_REAL=' || NVL(r.SEARCH_CONDITION_VC,'<NULL>') || ';RESULTADO=' || CASE WHEN r.REAL_TYPE=r.CONSTRAINT_TYPE AND r.STATUS='ENABLED' AND NVL(r.REAL_TABLA_REFERIDA,'<NULL>')=NVL(r.TABLA_REFERIDA,'<NULL>') THEN 'OK' ELSE 'DIFERENCIA' END);
    END LOOP;
    DBMS_OUTPUT.PUT_LINE('RESUMEN=CONSTRAINTS;OK=' || v_ok || '/' || v_total || ';CHECKS_DEBEN_REVISARSE_CONTRA_012');
END;
