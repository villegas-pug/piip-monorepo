            SELECT 'PROYECTO_PARTICIPANTE_UNIDAD','ID_PROY_PART_UNIDAD','NUMBER',12,0,NULL,NULL,'N',NULL FROM DUAL UNION ALL SELECT 'PROYECTO_PARTICIPANTE_UNIDAD','ID_PROYECTO','NUMBER',12,0,NULL,NULL,'N',NULL FROM DUAL UNION ALL SELECT 'PROYECTO_PARTICIPANTE_UNIDAD','ID_UNIDAD','NUMBER',10,0,NULL,NULL,'N',NULL FROM DUAL UNION ALL SELECT 'PROYECTO_PARTICIPANTE_UNIDAD','INICIO','DATE',NULL,NULL,NULL,NULL,'N',NULL FROM DUAL UNION ALL SELECT 'PROYECTO_PARTICIPANTE_UNIDAD','FIN','DATE',NULL,NULL,NULL,NULL,'Y',NULL FROM DUAL UNION ALL SELECT 'PROYECTO_PARTICIPANTE_UNIDAD','ID_ACTOR','NUMBER',10,0,NULL,NULL,'N',NULL FROM DUAL UNION ALL SELECT 'PROYECTO_PARTICIPANTE_UNIDAD','CREADO_POR','VARCHAR2',NULL,NULL,100,'C','N',NULL FROM DUAL UNION ALL SELECT 'PROYECTO_PARTICIPANTE_UNIDAD','FECHA_CREACION','TIMESTAMP',NULL,6,NULL,NULL,'N','SYSTIMESTAMP' FROM DUAL
