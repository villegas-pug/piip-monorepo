PROMPT [012-reconciliation] Columnas de referencias FK esperadas...
DECLARE
    v_ok PLS_INTEGER := 0; v_total PLS_INTEGER := 0;
BEGIN
    FOR r IN (
        WITH esperadas (CONSTRAINT_NAME,CHILD_COLUMN,PARENT_TABLE,PARENT_COLUMN) AS (
            SELECT 'FK_PP_USUARIO','ID_USUARIO','USUARIO','ID_USUARIO' FROM DUAL UNION ALL
            SELECT 'FK_PPP_PROYECTO','ID_PROYECTO','PROYECTO','ID_PROYECTO' FROM DUAL UNION ALL
            SELECT 'FK_PPP_PARTICIPANTE','ID_PARTICIPANTE','PARTICIPANTE_PERSONA','ID_PARTICIPANTE' FROM DUAL UNION ALL
            SELECT 'FK_PPP_ACTOR','ID_ACTOR','USUARIO','ID_USUARIO' FROM DUAL UNION ALL
            SELECT 'FK_PPU_PROYECTO','ID_PROYECTO','PROYECTO','ID_PROYECTO' FROM DUAL UNION ALL
            SELECT 'FK_PPU_UNIDAD','ID_UNIDAD','UNIDAD_EJECUTORA','ID_UNIDAD' FROM DUAL UNION ALL
            SELECT 'FK_PPU_ACTOR','ID_ACTOR','USUARIO','ID_USUARIO' FROM DUAL
        )
        SELECT e.*, cc.COLUMN_NAME REAL_CHILD_COLUMN, pc.TABLE_NAME REAL_PARENT_TABLE,
               pc.COLUMN_NAME REAL_PARENT_COLUMN
          FROM esperadas e
          LEFT JOIN USER_CONSTRAINTS c ON c.CONSTRAINT_NAME=e.CONSTRAINT_NAME
          LEFT JOIN USER_CONS_COLUMNS cc ON cc.CONSTRAINT_NAME=c.CONSTRAINT_NAME AND cc.POSITION=1
          LEFT JOIN USER_CONS_COLUMNS pc ON pc.CONSTRAINT_NAME=c.R_CONSTRAINT_NAME AND pc.POSITION=1
         ORDER BY e.CONSTRAINT_NAME
    ) LOOP
        v_total:=v_total+1;
        IF r.REAL_CHILD_COLUMN=r.CHILD_COLUMN AND r.REAL_PARENT_TABLE=r.PARENT_TABLE AND r.REAL_PARENT_COLUMN=r.PARENT_COLUMN THEN v_ok:=v_ok+1; END IF;
        DBMS_OUTPUT.PUT_LINE('TIPO=REFERENCIA;FK=' || r.CONSTRAINT_NAME || ';COLUMNA_HIJA_ESPERADA=' || r.CHILD_COLUMN || ';COLUMNA_HIJA_REAL=' || NVL(r.REAL_CHILD_COLUMN,'<NULL>') || ';TABLA_PADRE_ESPERADA=' || r.PARENT_TABLE || ';TABLA_PADRE_REAL=' || NVL(r.REAL_PARENT_TABLE,'<NULL>') || ';COLUMNA_PADRE_ESPERADA=' || r.PARENT_COLUMN || ';COLUMNA_PADRE_REAL=' || NVL(r.REAL_PARENT_COLUMN,'<NULL>') || ';RESULTADO=' || CASE WHEN r.REAL_CHILD_COLUMN=r.CHILD_COLUMN AND r.REAL_PARENT_TABLE=r.PARENT_TABLE AND r.REAL_PARENT_COLUMN=r.PARENT_COLUMN THEN 'OK' ELSE 'DIFERENCIA' END);
    END LOOP;
    DBMS_OUTPUT.PUT_LINE('RESUMEN=REFERENCIAS;OK=' || v_ok || '/' || v_total);
END;
