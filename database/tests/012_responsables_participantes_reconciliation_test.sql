PROMPT [012-reconciliation] Columnas y orden de PK/UK/indices auxiliares esperados...
DECLARE
    v_ok PLS_INTEGER := 0; v_total PLS_INTEGER := 0;
BEGIN
    FOR r IN (
        WITH esperadas (INDEX_NAME,TABLE_NAME,UNIQUENESS,COLUMN_POSITION,COLUMN_NAME) AS (
            SELECT 'PK_PARTICIPANTE_PERSONA','PARTICIPANTE_PERSONA','UNIQUE',1,'ID_PARTICIPANTE' FROM DUAL UNION ALL SELECT 'IDX_PP_USUARIO','PARTICIPANTE_PERSONA','NONUNIQUE',1,'ID_USUARIO' FROM DUAL UNION ALL SELECT 'PK_PROYECTO_PARTICIPANTE_PERSONA','PROYECTO_PARTICIPANTE_PERSONA','UNIQUE',1,'ID_PROY_PART_PERSONA' FROM DUAL UNION ALL SELECT 'UK_PPP_PROY_PART','PROYECTO_PARTICIPANTE_PERSONA','UNIQUE',1,'ID_PROYECTO' FROM DUAL UNION ALL SELECT 'UK_PPP_PROY_PART','PROYECTO_PARTICIPANTE_PERSONA','UNIQUE',2,'ID_PARTICIPANTE' FROM DUAL UNION ALL SELECT 'IDX_PPP_PROYECTO','PROYECTO_PARTICIPANTE_PERSONA','NONUNIQUE',1,'ID_PROYECTO' FROM DUAL UNION ALL SELECT 'PK_PROYECTO_PARTICIPANTE_UNIDAD','PROYECTO_PARTICIPANTE_UNIDAD','UNIQUE',1,'ID_PROY_PART_UNIDAD' FROM DUAL UNION ALL SELECT 'UK_PPU_PROY_UNI','PROYECTO_PARTICIPANTE_UNIDAD','UNIQUE',1,'ID_PROYECTO' FROM DUAL UNION ALL SELECT 'UK_PPU_PROY_UNI','PROYECTO_PARTICIPANTE_UNIDAD','UNIQUE',2,'ID_UNIDAD' FROM DUAL UNION ALL SELECT 'IDX_PPU_PROYECTO','PROYECTO_PARTICIPANTE_UNIDAD','NONUNIQUE',1,'ID_PROYECTO' FROM DUAL
        )
        SELECT e.*, i.UNIQUENESS REAL_UNIQUENESS, i.STATUS, ic.COLUMN_NAME REAL_COLUMN_NAME FROM esperadas e LEFT JOIN USER_INDEXES i ON i.INDEX_NAME=e.INDEX_NAME AND i.TABLE_NAME=e.TABLE_NAME LEFT JOIN USER_IND_COLUMNS ic ON ic.INDEX_NAME=e.INDEX_NAME AND ic.COLUMN_POSITION=e.COLUMN_POSITION ORDER BY e.TABLE_NAME,e.INDEX_NAME,e.COLUMN_POSITION
    ) LOOP
        v_total:=v_total+1;
        IF r.REAL_UNIQUENESS=r.UNIQUENESS AND r.STATUS='VALID' AND r.REAL_COLUMN_NAME=r.COLUMN_NAME THEN v_ok:=v_ok+1; END IF;
        DBMS_OUTPUT.PUT_LINE('TIPO=INDICE;NOMBRE=' || r.INDEX_NAME || ';TABLA=' || r.TABLE_NAME || ';POSICION=' || r.COLUMN_POSITION || ';COLUMNA_ESPERADA=' || r.COLUMN_NAME || ';COLUMNA_REAL=' || NVL(r.REAL_COLUMN_NAME,'<NULL>') || ';UNICIDAD=' || NVL(r.REAL_UNIQUENESS,'<NULL>') || ';ESTADO=' || NVL(r.STATUS,'<NULL>') || ';RESULTADO=' || CASE WHEN r.REAL_UNIQUENESS=r.UNIQUENESS AND r.STATUS='VALID' AND r.REAL_COLUMN_NAME=r.COLUMN_NAME THEN 'OK' ELSE 'DIFERENCIA' END);
    END LOOP;
