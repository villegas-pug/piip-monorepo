PROMPT [012-reconciliation] Secuencias esperadas (sin consumir valores)...
DECLARE
    v_ok PLS_INTEGER := 0;
BEGIN
    FOR r IN (
        WITH esperadas (SEQUENCE_NAME) AS (SELECT 'SEQ_PARTICIPANTE_PERSONA' FROM DUAL UNION ALL SELECT 'SEQ_PROY_PART_PERSONA' FROM DUAL UNION ALL SELECT 'SEQ_PROY_PART_UNIDAD' FROM DUAL)
