         ORDER BY e.TABLE_NAME, e.COLUMN_NAME
    ) LOOP
        v_total := v_total + 1;
        IF r.REAL_DATA_TYPE = r.DATA_TYPE AND NVL(r.REAL_PRECISION,-1) = NVL(r.DATA_PRECISION,-1) AND NVL(r.REAL_SCALE,-1) = NVL(r.DATA_SCALE,-1) AND NVL(r.REAL_CHAR_LENGTH,-1) = NVL(r.CHAR_LENGTH,-1) AND NVL(r.REAL_CHAR_USED,'-') = NVL(r.CHAR_USED,'-') AND NVL(r.REAL_NULLABLE,'-') = r.NULLABLE THEN v_ok := v_ok + 1; END IF;
        DBMS_OUTPUT.PUT_LINE('TIPO=COLUMNA;TABLA=' || r.TABLE_NAME || ';COLUMNA=' || r.COLUMN_NAME || ';ESPERADO=' || r.DATA_TYPE || ';PRECISION=' || NVL(TO_CHAR(r.DATA_PRECISION),'<NULL>') || ';SCALE=' || NVL(TO_CHAR(r.DATA_SCALE),'<NULL>') || ';CHAR_LENGTH=' || NVL(TO_CHAR(r.CHAR_LENGTH),'<NULL>') || ';CHAR_USED=' || NVL(r.CHAR_USED,'<NULL>') || ';NULLABLE=' || r.NULLABLE || ';DEFAULT_DECLARADO=' || NVL(r.DEFAULT_ESPERADO,'<NULL>') || ';DEFAULT_REAL=NO_VERIFICADO_LONG' || ';RESULTADO=' || CASE WHEN r.REAL_DATA_TYPE = r.DATA_TYPE AND NVL(r.REAL_PRECISION,-1)=NVL(r.DATA_PRECISION,-1) AND NVL(r.REAL_SCALE,-1)=NVL(r.DATA_SCALE,-1) AND NVL(r.REAL_CHAR_LENGTH,-1)=NVL(r.CHAR_LENGTH,-1) AND NVL(r.REAL_CHAR_USED,'-')=NVL(r.CHAR_USED,'-') AND NVL(r.REAL_NULLABLE,'-')=r.NULLABLE THEN 'OK_SIN_DEFAULT' ELSE 'DIFERENCIA' END);
    END LOOP;
    DBMS_OUTPUT.PUT_LINE('RESUMEN=COLUMNAS;OK_SIN_DEFAULT=' || v_ok || '/' || v_total || ';DEFAULTS_REQUIEREN_REVISION_DBA_SEPARADA');
END;
